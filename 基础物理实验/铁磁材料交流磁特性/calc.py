import numpy as np
import matplotlib.pyplot as plt
from scipy.interpolate import CubicSpline

# ------------------------------------------------------
# 数据输入
# ------------------------------------------------------
H = np.array([
    19.5007539,22.31529569,23.36237226,24.14977383,25.25967499,
    26.83028983,34.02998827,39.65907187,43.71335232,51.70045234,
    62.82459373,71.20120623,87.17121796,107.9745351,136.1199531,
    178.6731446,216.6191992,253.9370079,312.8245937,388.716703
])

B = np.array([
    3.429341069,4.045793618,4.341069208,4.631164525,4.988603398,
    5.335681724,6.682552839,7.355988396,9.065478657,10.51595524,
    11.96643183,13.05428927,14.66017406,16.00704517,17.30211355,
    18.38997099,19.16701202,19.42602569,21.03191048,21.70534604
]) * 1e-3   # mT → T （若不需要可去掉 *1e-3）

mu = B / H  # μ(H)

# ------------------------------------------------------
# 三次样条插值（严格经过所有点）
# ------------------------------------------------------
cs_B = CubicSpline(H, B)
cs_mu = CubicSpline(H, mu)

H_dense = np.linspace(min(H), max(H), 500)
B_dense = cs_B(H_dense)
mu_dense = cs_mu(H_dense)

# ------------------------------------------------------
# 图 1：B–H 曲线
# ------------------------------------------------------
plt.figure(figsize=(7,5))
plt.scatter(H, B, label='data')
plt.plot(H_dense, B_dense, 'r')
plt.xlabel("H (A/m)")
plt.ylabel("B (T)")
plt.title("B–H Curve")
plt.grid(True)
plt.legend()
plt.tight_layout()
plt.show()

# ------------------------------------------------------
# 图 2：μ–H 曲线
# ------------------------------------------------------
plt.figure(figsize=(7,5))
plt.scatter(H, mu, label='data')
plt.plot(H_dense, mu_dense, 'r')
plt.xlabel("H (A/m)")
plt.ylabel("μ (H/m)")
plt.title("μ–H Curve")
plt.grid(True)
plt.legend()
plt.tight_layout()
plt.show()
